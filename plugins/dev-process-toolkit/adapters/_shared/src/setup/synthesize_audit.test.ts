describe("synthesizeAuditSection — STE-153 idempotent on conflicting reason (AC-STE-153.3)", () => {
  test("re-run with a different reason for the same (step, field) is a skip — original entry preserved verbatim", () => {
    const ctx = tmpClaudeMd(`${TOOLKIT_MARKER}\n\n# Project\n`);
    try {
      // First call: branch_template recorded with reason: "default applied".
      const firstRun: ResolvedSchemaLValues = {
        date: "2026-04-29",
        branchTemplate: { value: "feat/{ticket-id}-{slug}", reason: "default applied" },
      };
      const r1 = synthesizeAuditSection(ctx.path, firstRun);
      expect(r1.synthesized).toBe(1);
      expect(r1.skipped).toBe(0);

      // Second call: same (step, field) but reason: "user-supplied". Dedup
      // is on (step, field) only — the second call is a skip, not an
      // overwrite, preserving STE-108 append-only semantics.
      const secondRun: ResolvedSchemaLValues = {
        date: "2026-04-29",
        branchTemplate: { value: "feat/{ticket-id}-{slug}", reason: "user-supplied" },
      };
      const r2 = synthesizeAuditSection(ctx.path, secondRun);
      expect(r2.synthesized).toBe(0);
      expect(r2.skipped).toBe(1);

      const out = readFileSync(ctx.path, "utf-8");
      // Only one entry for (step:7c, branch_template), and it carries the
      // ORIGINAL reason — second call did not overwrite.
      const branchTemplateMatches = out.match(/step:7c \(branch_template\)/g);
      expect(branchTemplateMatches?.length).toBe(1);
      expect(out).toContain(`step:7c (branch_template) value:"feat/{ticket-id}-{slug}" reason:"default applied"`);
      expect(out).not.toContain(`step:7c (branch_template) value:"feat/{ticket-id}-{slug}" reason:"user-supplied"`);
    } finally {
      ctx.cleanup();
    }
  });
});
