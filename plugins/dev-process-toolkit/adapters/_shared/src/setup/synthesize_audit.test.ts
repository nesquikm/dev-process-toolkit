describe("synthesizeAuditSection — STE-153 user-supplied provenance (AC-STE-153.1)", () => {
  test("all four entries with reason: \"user-supplied\" → 4 synthesized, each carries the user-supplied reason", () => {
    const initial = [
      TOOLKIT_MARKER,
      "",
      "# Project",
      "",
      "## Task Tracking",
      "",
      "mode: linear",
      "branch_template: feat/{ticket-id}-{slug}",
      "",
      "## Docs",
      "",
      "user_facing_mode: true",
      "packages_mode: true",
      "changelog_ci_owned: true",
      "",
    ].join("\n");
    const ctx = tmpClaudeMd(initial);
    try {
      const allUserSupplied: ResolvedSchemaLValues = {
        date: "2026-04-29",
        branchTemplate: { value: "feat/{ticket-id}-{slug}", reason: "user-supplied" },
        docsUserFacing: { value: true, reason: "user-supplied" },
        docsPackages: { value: true, reason: "user-supplied" },
        docsChangelogCi: { value: true, reason: "user-supplied" },
      };
      const r = synthesizeAuditSection(ctx.path, allUserSupplied);
      expect(r.synthesized).toBe(4);
      expect(r.skipped).toBe(0);
      const out = readFileSync(ctx.path, "utf-8");
      // All four bullets carry the user-supplied reason — no "default applied" present.
      const userSuppliedMatches = out.match(/reason:"user-supplied"/g);
      expect(userSuppliedMatches?.length).toBe(4);
      expect(out.includes(`reason:"default applied"`)).toBe(false);
      // Field-level coverage.
      expect(out).toMatch(/step:7c \(branch_template\) value:"feat\/{ticket-id}-{slug}" reason:"user-supplied"/);
      expect(out).toMatch(/step:7d \(docs\.user_facing_mode\) value:true reason:"user-supplied"/);
      expect(out).toMatch(/step:7d \(docs\.packages_mode\) value:true reason:"user-supplied"/);
      expect(out).toMatch(/step:7d \(docs\.changelog_ci_owned\) value:true reason:"user-supplied"/);
    } finally {
      ctx.cleanup();
    }
  });
});

