describe("AC-STE-394.1 — Step 0: a toolkit-managed section admits the walk", () => {
  const managedShapes: Array<[string, string]> = [
    ["the `/setup` generation marker", MANAGED_BY_MARKER],
    ["the managed `## Task Tracking` section", MANAGED_BY_TASK_TRACKING],
    ["the managed `## Docs` section", MANAGED_BY_DOCS],
  ];

  for (const [label, claudeMd] of managedShapes) {
    test(`${label} makes the tree in-scope, so the detector walk runs`, async () => {
      const root = makeTree({ "CLAUDE.md": claudeMd });
      plantLegacyLocks(root);
      const r = await runUpgradeStalenessProbe(root);
      expect(r.rows.length).toBe(1);
      expect(r.rows[0]!.startsWith("m104-legacy-state (2.46.0): ")).toBe(true);
    });
  }

  test("the walk visits every registry entry, in registry order", async () => {
    const root = makeTree({ "CLAUDE.md": MANAGED_BY_TASK_TRACKING });
    const visited: string[] = [];
    const spyRegistry: MigrationEntry[] = ["gamma", "alpha", "beta"].map((id, i) => ({
      id,
      introduced_in: `1.${i}.0`,
      title: id,
      kind: "script",
      detect: () => {
        visited.push(id);
        return { applies: false, evidence: [] };
      },
      apply: () => ({ changed: [], summary: "" }),
    }));
    await runUpgradeStalenessProbe(root, spyRegistry);
    // Registry order, NOT alphabetical — the registry is version-ordered.
    expect(visited).toEqual(["gamma", "alpha", "beta"]);
  });

  test("rows render in registry order, one per applying entry, none for the rest", async () => {
    const root = makeTree({ "CLAUDE.md": MANAGED_BY_TASK_TRACKING });
    const registry: MigrationEntry[] = [
      fixtureEntry("zulu-first", "1.0.0", ["z fired"]),
      fixtureEntry("quiet-entry", "1.1.0", []), // applies:false — renders nothing
      fixtureEntry("alpha-last", "1.2.0", ["a fired"]),
    ];
    const r = await runUpgradeStalenessProbe(root, registry);
    expect(r.rows).toEqual([
      "zulu-first (1.0.0): z fired",
      "alpha-last (1.2.0): a fired",
    ]);
  });

  test("the probe NEVER emits GATE FAILED — violations stay empty even with every entry firing", async () => {
    const root = makeTree({ "CLAUDE.md": MANAGED_BY_TASK_TRACKING });
    plantAllM104State(root);
    plantV1Orphan(root);
    plantStaleHookEntry(root);
    plantMonolith(root);
    const r = await runUpgradeStalenessProbe(root);
    expect(r.rows.length).toBeGreaterThan(0);
    expect(r.violations).toEqual([]);
  });
});

