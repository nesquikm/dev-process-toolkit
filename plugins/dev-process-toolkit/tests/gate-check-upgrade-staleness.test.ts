describe("AC-STE-394.5 — remedy literal fires exactly once, and only with rows", () => {
  test("the exported literal is byte-exact", () => {
    expect(UPGRADE_STALENESS_REMEDY).toBe(
      "Remedy: run /dev-process-toolkit:upgrade to apply these migrations (Claude can invoke it; it is not on the slash menu).",
    );
  });

  test("with >= 1 row the NOTES block CLOSES with the remedy, exactly once", async () => {
    const root = makeTree({ "CLAUDE.md": MANAGED_BY_TASK_TRACKING });
    const r = await runUpgradeStalenessProbe(root, [
      fixtureEntry("a-entry", "1.0.0", ["a"]),
      fixtureEntry("b-entry", "1.1.0", ["b"]),
    ]);
    expect(r.rows.length).toBe(2);
    expect(r.notes).toEqual([...r.rows, UPGRADE_STALENESS_REMEDY]);
    expect(r.notes[r.notes.length - 1]).toBe(UPGRADE_STALENESS_REMEDY);
    const occurrences = r.notes.filter((n) => n === UPGRADE_STALENESS_REMEDY).length;
    expect(occurrences).toBe(1);
    // Once per RUN, not once per row.
    const inline = r.notes.join("\n").split(UPGRADE_STALENESS_REMEDY).length - 1;
    expect(inline).toBe(1);
  });

  test("with ZERO rows the remedy is never emitted", async () => {
    const root = makeTree({ "CLAUDE.md": MANAGED_BY_TASK_TRACKING });
    const r = await runUpgradeStalenessProbe(root, [fixtureEntry("quiet", "1.0.0", [])]);
    expect(r.rows).toEqual([]);
    expect(r.notes).toEqual([]);
    expect(r.notes.join("\n")).not.toContain("dev-process-toolkit:upgrade");
  });

  test("the short-circuit path emits no remedy either", async () => {
    const root = makeTree({ "CLAUDE.md": UNMANAGED });
    plantAllM104State(root);
    const r = await runUpgradeStalenessProbe(root);
    expect(r.notes.join("\n")).not.toContain(UPGRADE_STALENESS_REMEDY);
  });
});

