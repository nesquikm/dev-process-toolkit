describe("AC-STE-394.2 — row shape `<id> (<introduced_in>): <evidence joined by \"; \">`", () => {
  test("single-evidence row is exactly `<id> (<version>): <evidence>`", async () => {
    const root = makeTree({ "CLAUDE.md": MANAGED_BY_TASK_TRACKING });
    const r = await runUpgradeStalenessProbe(root, [
      fixtureEntry("solo-entry", "3.1.4", ["only one thing"]),
    ]);
    expect(r.rows).toEqual(["solo-entry (3.1.4): only one thing"]);
  });

  test("multi-evidence joins with EXACTLY `\"; \"` — no trailing separator, no bullets", async () => {
    const root = makeTree({ "CLAUDE.md": MANAGED_BY_TASK_TRACKING });
    const r = await runUpgradeStalenessProbe(root, [
      fixtureEntry("triple", "2.0.0", ["one", "two", "three"]),
    ]);
    expect(r.rows).toEqual(["triple (2.0.0): one; two; three"]);
  });

  test("evidence order is the detector's returned order — NEVER re-sorted", async () => {
    const root = makeTree({ "CLAUDE.md": MANAGED_BY_TASK_TRACKING });
    // Deliberately anti-alphabetical: a sorting implementation cannot pass.
    const r = await runUpgradeStalenessProbe(root, [
      fixtureEntry("unsorted", "1.0.0", ["zebra", "alpha", "middle"]),
    ]);
    expect(r.rows).toEqual(["unsorted (1.0.0): zebra; alpha; middle"]);
  });

  test("evidence is NEVER truncated — every item survives, however many or long", async () => {
    const root = makeTree({ "CLAUDE.md": MANAGED_BY_TASK_TRACKING });
    const evidence = Array.from({ length: 8 }, (_, i) => `${"e".repeat(120)}-${i}`);
    const r = await runUpgradeStalenessProbe(root, [fixtureEntry("verbose", "1.0.0", evidence)]);
    expect(r.rows).toEqual([`verbose (1.0.0): ${evidence.join("; ")}`]);
    for (const item of evidence) expect(r.rows[0]!).toContain(item);
    expect(r.rows[0]!).not.toContain("…");
    expect(r.rows[0]!).not.toContain("...");
  });

  test("the LIVE m104-legacy-state row pins the shape against real detector prose", async () => {
    const root = makeTree({ "CLAUDE.md": MANAGED_BY_TASK_TRACKING });
    plantAllM104State(root);
    const r = await runUpgradeStalenessProbe(root);
    const entry = liveEntry("m104-legacy-state");
    const detected = entry.detect(root);
    // Three signals planted ⇒ three evidence strings ⇒ two `"; "` separators.
    expect(detected.evidence.length).toBe(3);
    const row = r.rows.find((l) => l.startsWith("m104-legacy-state "));
    expect(row).toBeDefined();
    expect(row).toBe(
      `${entry.id} (${entry.introduced_in}): ${detected.evidence.join("; ")}`,
    );
    expect(row!.split("; ").length).toBe(3);
  });
});

