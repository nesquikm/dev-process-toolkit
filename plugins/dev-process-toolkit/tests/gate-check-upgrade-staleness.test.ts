/** A synthetic entry whose detector returns a fixed verdict. */
function fixtureEntry(
  id: string,
  introducedIn: string,
  evidence: string[],
): MigrationEntry {
  return {
    id,
    introduced_in: introducedIn,
    title: `${id} (fixture)`,
    kind: "script",
    detect: () => ({ applies: evidence.length > 0, evidence }),
    apply: () => ({ changed: [], summary: "" }),
  };
}

/** Recursive `relpath → content|<dir>` snapshot, for the no-writes purity check. */
function snapshot(root: string): Record<string, string> {
  const out: Record<string, string> = {};
  const walk = (dir: string): void => {
    for (const name of readdirSync(dir).sort()) {
      const abs = join(dir, name);
      const rel = relative(root, abs);
      if (statSync(abs).isDirectory()) {
        out[rel] = "<dir>";
        walk(abs);
      } else {
        out[rel] = readFileSync(abs, "utf-8");
      }
    }
  };
  walk(root);
  return out;
}

/** The live registry entry with this id — the detector prose belongs to it, not to us. */
function liveEntry(id: string): MigrationEntry {
  const entry = MIGRATIONS.find((e) => e.id === id);
  if (entry === undefined) throw new Error(`live registry has no entry "${id}"`);
  return entry;
}

