// M109 STE-394 — /gate-check probe `upgrade_staleness` (probe #69).
//
// A warn-only probe that walks the migration registry's detectors and reports
// every consumer artifact that has fallen behind current toolkit conventions.
// It never fails the gate: pre-existing drift must not block a merge.
//
// AC map:
//   AC-STE-394.1 — Step 0 applicability short-circuit (no toolkit-managed
//                  CLAUDE.md section ⇒ zero rows, ZERO detectors walked), then
//                  a registry-order detector walk; one row per `applies: true`
//                  entry, rendered under GATE PASSED WITH NOTES. Never FAILED.
//   AC-STE-394.2 — byte-pinned row shape
//                  `<entry-id> (<introduced_in>): <evidence joined by "; ">`,
//                  evidence in the detector's returned order, never re-sorted,
//                  never truncated.
//   AC-STE-394.3 — purity: pure file reads + a registry module load. No git,
//                  no network, no LLM judgment, no writes.
//   AC-STE-394.4 — vacuous in both directions, including the consumer-safety
//                  case: legacy artifacts on disk + no toolkit-managed
//                  CLAUDE.md section ⇒ zero rows and no error.
//   AC-STE-394.5 — the NOTES block closes with the byte-pinned remedy literal,
//                  exactly once with >= 1 row, never with zero rows.
//   AC-STE-394.9 — non-vacuity in both directions across the LIVE registry:
//                  a clean tree renders nothing; a tree carrying each entry's
//                  legacy state renders exactly that entry's row.
//
// Report contract pinned here (shape precedent: `runMigrationCoverageProbe`):
//   runUpgradeStalenessProbe(projectRoot, registry?)
//     => {
//          rows:       string[];   // one byte-pinned row per applying entry
//          notes:      string[];   // the rendered NOTES block:
//                                  //   [...rows, UPGRADE_STALENESS_REMEDY]
//                                  //   when rows is non-empty, else []
//          violations: never[];    // ALWAYS empty — the probe never FAILs
//        }
//   plus `PROBE_ID` and the `UPGRADE_STALENESS_REMEDY` literal.
//
// The `registry` parameter defaults to the live MIGRATIONS list; tests inject
// synthetic registries to pin ordering, the `"; "` join, and the no-sort /
// no-truncate rules independently of any live detector's prose. Every call is
// `await`ed so the contract holds whether the implementation is sync or async.
//
// Fixture discipline (FR § Testing): mkdtempSync trees, per STE-391. Retired
// path literals are composed through `legacy_paths` helpers rather than
// spelled out, so this file needs no STE-384 carve-out of its own.

