// ---------------------------------------------------------------------------
// HARDENING — a throwing detector must not crash the probe
//
// Found and reproduced during /implement Phase 3 Stage C (error-path audit).
// `upgrade_staleness.ts` calls `entry.detect(projectRoot)` with no guard, so a
// detector that throws propagates out of `runUpgradeStalenessProbe` and takes
// the whole gate run down with it. That is strictly WORSE than the GATE FAILED
// AC-STE-394.1 forbids: a FAILED verdict is a verdict, a thrown probe is a
// crashed gate.
//
// The failure is reachable against the LIVE registry, not only synthetically:
// `m104-legacy-state`'s detector reads the consumer's ROOT `.gitignore` via
// `readLines` → `readFileSync`, which throws EACCES on an unreadable file.
//
// Contract these tests pin:
//   1. a throwing detector never rejects the probe — it resolves;
//   2. `violations` stays empty (AC-STE-394.1's never-FAIL contract survives
//      the error path — degrade, do not escalate);
//   3. the failure is NOT swallowed silently — the erroring entry surfaces a
//      row, and that row stays inside the AC-STE-394.2 byte-pinned shape
//      `<entry-id> (<introduced_in>): <evidence>`, with the thrown message
//      carried as the evidence text;
//   4. one throwing entry does not suppress the entries around it, in either
//      registry position;
//   5. the AC-STE-394.5 remedy still fires exactly once when rows exist,
//      error rows included.
// ---------------------------------------------------------------------------

/** Root bypasses file permission bits, so chmod-based cases cannot go RED there. */
const isRoot = process.getuid?.() === 0;

/** A synthetic entry whose detector throws instead of returning a verdict. */
function throwingEntry(id: string, introducedIn: string, message: string): MigrationEntry {
  return {
    id,
    introduced_in: introducedIn,
    title: `${id} (throwing fixture)`,
    kind: "script",
    detect: () => {
      throw new Error(message);
    },
    apply: () => ({ changed: [], summary: "" }),
  };
}

/**
 * Run the probe and report resolve-vs-reject as DATA rather than letting a
 * rejection blow up the test body — so a regression reads as "PROBE THREW"
 * instead of an opaque unhandled error.
 */
async function runOrCapture(
  root: string,
  registry?: readonly MigrationEntry[],
): Promise<{ report: UpgradeStalenessReport | null; error: unknown }> {
  try {
    return { report: await runUpgradeStalenessProbe(root, registry), error: null };
  } catch (error) {
    return { report: null, error };
  }
}

describe("HARDENING — a throwing detector degrades to a row, never to a crash", () => {
  const BOOM = "detector exploded";

  test("the probe RESOLVES — a throwing detector does not reject the run", async () => {
    const root = makeTree({ "CLAUDE.md": MANAGED_BY_TASK_TRACKING });
    const { report, error } = await runOrCapture(root, [throwingEntry("boom-entry", "1.0.0", BOOM)]);
    expect(error).toBeNull();
    expect(report).not.toBeNull();
    expect(Array.isArray(report!.rows)).toBe(true);
  });

  test("the never-FAIL contract holds under detector error — violations stay empty", async () => {
    const root = makeTree({ "CLAUDE.md": MANAGED_BY_TASK_TRACKING });
    const { report, error } = await runOrCapture(root, [throwingEntry("boom-entry", "1.0.0", BOOM)]);
    expect(error).toBeNull();
    expect(report!.violations).toEqual([]);
  });

  test("the error is NOT silent — the erroring entry surfaces exactly one row", async () => {
    const root = makeTree({ "CLAUDE.md": MANAGED_BY_TASK_TRACKING });
    const { report, error } = await runOrCapture(root, [throwingEntry("boom-entry", "1.0.0", BOOM)]);
    expect(error).toBeNull();
    expect(report!.rows.length).toBe(1);
  });

  test("the error row keeps the AC-STE-394.2 byte-pinned shape and carries the message", async () => {
    const root = makeTree({ "CLAUDE.md": MANAGED_BY_TASK_TRACKING });
    const { report, error } = await runOrCapture(root, [throwingEntry("boom-entry", "4.5.6", BOOM)]);
    expect(error).toBeNull();
    const row = report!.rows[0]!;
    // Same shape the dogfood block pins for healthy rows: `<id> (<version>): <evidence>`.
    expect(row).toMatch(/^[a-z0-9-]+ \(\d+\.\d+\.\d+\): .+/);
    expect(row.startsWith("boom-entry (4.5.6): ")).toBe(true);
    // The evidence text must carry the thrown message — otherwise the row says
    // "something is wrong here" with no way to tell what.
    expect(row).toContain(BOOM);
    // ...and must read as an error, not as ordinary legacy-state evidence.
    expect(row).toMatch(/error/i);
    // The shape is one line: a raw multi-line dump would break the NOTES block.
    expect(row).not.toContain("\n");
  });

  test("a thrown non-Error value still renders a row instead of crashing", async () => {
    const root = makeTree({ "CLAUDE.md": MANAGED_BY_TASK_TRACKING });
    const stringThrower: MigrationEntry = {
      id: "string-thrower",
      introduced_in: "2.0.0",
      title: "throws a bare string",
      kind: "script",
      detect: () => {
        throw "bare string failure";
      },
      apply: () => ({ changed: [], summary: "" }),
    };
    const { report, error } = await runOrCapture(root, [stringThrower]);
    expect(error).toBeNull();
    expect(report!.rows.length).toBe(1);
    expect(report!.rows[0]!.startsWith("string-thrower (2.0.0): ")).toBe(true);
    expect(report!.rows[0]!).toContain("bare string failure");
    expect(report!.violations).toEqual([]);
  });

  test("a throwing FIRST entry does not suppress the applying entry after it", async () => {
    const root = makeTree({ "CLAUDE.md": MANAGED_BY_TASK_TRACKING });
    const { report, error } = await runOrCapture(root, [
      throwingEntry("boom-entry", "1.0.0", BOOM),
      fixtureEntry("healthy-entry", "1.1.0", ["legacy thing present"]),
    ]);
    expect(error).toBeNull();
    expect(report!.rows.length).toBe(2);
    // The healthy row renders EXACTLY as it would with no throwing sibling.
    expect(report!.rows[1]).toBe("healthy-entry (1.1.0): legacy thing present");
    expect(report!.rows[0]!.startsWith("boom-entry (1.0.0): ")).toBe(true);
  });

  test("a throwing LAST entry renders after the applying one — both rows, registry order", async () => {
    const root = makeTree({ "CLAUDE.md": MANAGED_BY_TASK_TRACKING });
    const { report, error } = await runOrCapture(root, [
      fixtureEntry("healthy-entry", "1.1.0", ["legacy thing present"]),
      throwingEntry("boom-entry", "1.2.0", BOOM),
    ]);
    expect(error).toBeNull();
    expect(report!.rows.length).toBe(2);
    expect(report!.rows[0]).toBe("healthy-entry (1.1.0): legacy thing present");
    expect(report!.rows[1]!.startsWith("boom-entry (1.2.0): ")).toBe(true);
    expect(report!.rows[1]!).toContain(BOOM);
  });

  test("a non-applying entry between two throwers still renders nothing", async () => {
    const root = makeTree({ "CLAUDE.md": MANAGED_BY_TASK_TRACKING });
    const { report, error } = await runOrCapture(root, [
      throwingEntry("boom-one", "1.0.0", "first boom"),
      fixtureEntry("quiet-entry", "1.1.0", []),
      throwingEntry("boom-two", "1.2.0", "second boom"),
    ]);
    expect(error).toBeNull();
    const ids = report!.rows.map((row) => row.slice(0, row.indexOf(" (")));
    expect(ids).toEqual(["boom-one", "boom-two"]);
  });

  test("the remedy literal fires exactly once when the only rows are error rows", async () => {
    const root = makeTree({ "CLAUDE.md": MANAGED_BY_TASK_TRACKING });
    const { report, error } = await runOrCapture(root, [
      throwingEntry("boom-one", "1.0.0", "first boom"),
      throwingEntry("boom-two", "1.1.0", "second boom"),
    ]);
    expect(error).toBeNull();
    expect(report!.rows.length).toBe(2);
    expect(report!.notes).toEqual([...report!.rows, UPGRADE_STALENESS_REMEDY]);
    const occurrences = report!.notes.filter((n) => n === UPGRADE_STALENESS_REMEDY).length;
    expect(occurrences).toBe(1);
  });

  test("the Step 0 short-circuit still runs BEFORE any detector can throw", async () => {
    // An unmanaged tree must stay clean even when every registry entry would
    // explode — the short-circuit is what keeps the probe silent on trees the
    // toolkit does not own, and an error path must not smuggle rows past it.
    const root = makeTree({ "CLAUDE.md": UNMANAGED });
    const { report, error } = await runOrCapture(root, [throwingEntry("boom-entry", "1.0.0", BOOM)]);
    expect(error).toBeNull();
    expect(report!.rows).toEqual([]);
    expect(report!.notes).toEqual([]);
    expect(report!.violations).toEqual([]);
  });
});

describe("HARDENING — the LIVE registry survives an unreadable consumer file", () => {
  // Reproduces the real-world reachable case: `m104-legacy-state`'s detector
  // reads the ROOT `.gitignore`, so a tree whose `.gitignore` is chmod 000
  // throws EACCES out of the live detector walk. Skipped as root, which
  // bypasses permission bits (same convention as
  // `tests/m108-ste-392-monolith-flow.test.ts`).

  /** Managed tree whose root `.gitignore` exists but cannot be read. */
  function makeUnreadableGitignoreTree(): { root: string; gitignore: string } {
    const root = makeTree({
      "CLAUDE.md": MANAGED_BY_TASK_TRACKING,
      ".gitignore": "node_modules/\ndist/\n",
    });
    const gitignore = join(root, ".gitignore");
    chmodSync(gitignore, 0o000);
    return { root, gitignore };
  }

  test.skipIf(isRoot)("the live probe RESOLVES instead of throwing EACCES", async () => {
    const { root, gitignore } = makeUnreadableGitignoreTree();
    try {
      // Precondition: the live detector really does throw on this tree, so this
      // case is non-vacuous — it is the crash, not a hypothetical.
      expect(() => liveEntry("m104-legacy-state").detect(root)).toThrow();
      const { report, error } = await runOrCapture(root);
      expect(error).toBeNull();
      expect(report!.violations).toEqual([]);
    } finally {
      chmodSync(gitignore, 0o644);
    }
  });

  test.skipIf(isRoot)("the EACCES surfaces as a byte-pinned m104-legacy-state row", async () => {
    const { root, gitignore } = makeUnreadableGitignoreTree();
    try {
      const { report, error } = await runOrCapture(root);
      expect(error).toBeNull();
      const entry = liveEntry("m104-legacy-state");
      const row = report!.rows.find((l) => l.startsWith(`${entry.id} (${entry.introduced_in}): `));
      expect(row).toBeDefined();
      expect(row!).toMatch(/^[a-z0-9-]+ \(\d+\.\d+\.\d+\): .+/);
      // The operator must be able to see WHICH file the probe could not read.
      expect(row!).toMatch(/EACCES|permission denied/i);
      expect(row!).toContain(".gitignore");
      // The advisory still closes with the remedy, exactly once.
      expect(report!.notes).toEqual([...report!.rows, UPGRADE_STALENESS_REMEDY]);
    } finally {
      chmodSync(gitignore, 0o644);
    }
  });

  test.skipIf(isRoot)("healthy live entries still report normally alongside the erroring one", async () => {
    const { root, gitignore } = makeUnreadableGitignoreTree();
    try {
      plantV1Orphan(root); // v1-orphans applies and must survive m104's throw
      const { report, error } = await runOrCapture(root);
      expect(error).toBeNull();
      const v1 = liveEntry("v1-orphans");
      const row = report!.rows.find((l) => l.startsWith(`${v1.id} (${v1.introduced_in}): `));
      expect(row).toBeDefined();
      expect(row).toBe(`${v1.id} (${v1.introduced_in}): ${v1.detect(root).evidence.join("; ")}`);
      expect(report!.violations).toEqual([]);
    } finally {
      chmodSync(gitignore, 0o644);
    }
  });
});
